/**
 * Markdown 記号を除去してプレーンテキスト化する。
 *
 * 目的: 分身AIチャット（whitespace-pre-wrap表示）や admin の章本文表示など、
 * 「リッチレンダリングしない場所」で `**xxx**` `## 見出し` `---` 等の
 * 記号が literal で露出するのを防ぐ。
 *
 * 方針: 「全部が太字になったりしないように綺麗に修正」（YO 2026-05-17 指示）
 *   → 太字レンダリングは実装せず、記号のみを除去してプレーンテキスト化する。
 *   ペアになってない記号も全て除去するので、太字暴発のリスクなし。
 *
 * PDF の MarkdownChapter.tsx は別系統（ペア対応で実際の太字描画）。
 * このヘルパは「太字描画できない表示先」専用。
 */
export function stripMarkdownToPlain(text: string | null | undefined): string {
  if (!text) return '';
  let s = text;
  // 行頭の見出し記号 ##, ### 等を除去（先頭スペースも一緒に）
  s = s.replace(/^#{1,6}\s+/gm, '');
  // 行中に紛れ込んだ ##〜###### を除去（半角空白で挟まれたパターン）
  // ※ 単独の # はSNSタグ可能性があるので2個以上のみ対象
  s = s.replace(/\s#{2,6}\s/g, ' ');
  // 水平線 ---, ***, ___ を空行に
  s = s.replace(/^(\s*)([-*_]){3,}(\s*)$/gm, '');
  // 太字 **xxx** → xxx
  s = s.replace(/\*\*([^*\n]+?)\*\*/g, '$1');
  // 残った孤立 ** を除去（閉じてない太字記号）
  s = s.replace(/\*\*/g, '');
  // イタリック *xxx* → xxx（行内のみ・前後が英数や*でないこと）
  s = s.replace(/(^|[^\w*])\*([^*\n]+?)\*(?=[^\w*]|$)/g, '$1$2');
  return s;
}
